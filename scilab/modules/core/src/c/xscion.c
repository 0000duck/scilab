int INXscilab=0;
/*-----------------------------------------------------------------------------------*/ 
int C2F(xscion) (int *i)
{
  *i = INXscilab;
  return (0);
}
/*-----------------------------------------------------------------------------------*/ 
void SetXsciOn(void)
{
	#if _MSC_VER
	 extern void switch_rlgets (int i);
	 switch_rlgets (1);
	#endif
	INXscilab = 1;
}
/*-----------------------------------------------------------------------------------*/ 
int getINXscilab(void)
{
	return INXscilab;
}
