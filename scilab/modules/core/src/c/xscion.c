int C2F(xscion) (int *i)
{
  if ( getScilabMode() == SCILAB_STD) *i=1;
  else *i=0;
  return 0;
}
/*-----------------------------------------------------------------------------------*/ 
int getINXscilab(void)
{
	int retVal=0;

	if ( getScilabMode() == SCILAB_STD ) retVal=1;
	else retVal=0;

	return retVal;
}
