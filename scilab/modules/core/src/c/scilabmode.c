#include "scilabmode.h"
/*-----------------------------------------------------------------------------------*/ 
/* default mode is STD */
static BOOL bNWNImode=FALSE;
static BOOL bNWmode=FALSE;
static BOOL bSTDmode=TRUE;
/*-----------------------------------------------------------------------------------*/ 
BOOL setNWNImode(void)
{
	bNWNImode=TRUE;
	bNWmode=FALSE;
	bSTDmode=FALSE;

	return TRUE;
}
/*-----------------------------------------------------------------------------------*/ 
BOOL setNWmode(void)
{
	bNWmode=TRUE;
	bNWNImode=FALSE;
	bSTDmode=FALSE;

	return TRUE;
}
/*-----------------------------------------------------------------------------------*/ 
BOOL setSTDmode(void)
{
	bNWmode=FALSE;
	bNWNImode=FALSE;
	bSTDmode=TRUE;

	return TRUE;
}
/*-----------------------------------------------------------------------------------*/ 
BOOL isSTDmode(void)
{
	return bSTDmode;
}
/*-----------------------------------------------------------------------------------*/ 
BOOL isNWmode(void)
{
	return bNWmode;
}
/*-----------------------------------------------------------------------------------*/ 
BOOL isNWNImode(void)
{
	return bNWNImode;
}
/*-----------------------------------------------------------------------------------*/ 
int C2F(isstdmode)(int *ok)
{
	*ok=(int)bSTDmode;
	return 1;
}
/*-----------------------------------------------------------------------------------*/ 
int C2F(isnwmode)(int *ok)
{
	*ok=(int)bNWmode;
	return 1;
}
/*-----------------------------------------------------------------------------------*/ 
int C2F(isnwnimode)(int *ok)
{
	*ok=(int)bNWNImode;
	return 1;
}
/*-----------------------------------------------------------------------------------*/ 

