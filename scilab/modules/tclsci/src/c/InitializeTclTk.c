BOOL InitializeTclTk(void)
{
	if ( getScilabMode() != SCILAB_NWNI )
	{
	  setTkStarted(TRUE);
	  if ( OpenTCLsci()!=0 )
	    {
	      setTkStarted(FALSE);
	    }
	}
	return isTkStarted();
}
