#include "filesmanagement.h"
/*-----------------------------------------------------------------------------------*/
void C2F(getfiledesc)(integer *fd) 
{
	int i ;
	for ( i = 1 ; i < GetMaximumFileOpenedInScilab() ; i++) 
	{
		if ( GetFileTypeOpenedInScilab(i) == 0 ) 
		{
			*fd = i;
			return ;
		}
	}
	*fd = -1;
}
