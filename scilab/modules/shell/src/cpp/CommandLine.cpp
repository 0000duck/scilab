#include "CommandLine.hxx"
#include "MALLOC.h"
/*------------------------------------------------------------------------*/
CommandLine::CommandLine(void)
{
}
/*------------------------------------------------------------------------*/
CommandLine::CommandLine(std::string line_)
{
	this->Command = line_ ;
}
/*------------------------------------------------------------------------*/
CommandLine::~CommandLine()
{
	Command.erase();
}
/*------------------------------------------------------------------------*/
std::string CommandLine::get(void)
{
	return Command ;
}
/*------------------------------------------------------------------------*/
BOOL CommandLine::set(std::string line_)
{
	BOOL bOK = FALSE;
	if (!line_.empty())
	{
		this->Command = line_ ;
		bOK = TRUE;
	}
	return bOK;
}
