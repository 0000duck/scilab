#include <Windows.h>
#include "gotoxy_nw.h"
/*-----------------------------------------------------------------------------------*/
void gotoxy_nw(int x, int y)
{
	COORD c;
	c.X = x - 1;
	c.Y = y - 1;
	SetConsoleCursorPosition (GetStdHandle(STD_OUTPUT_HANDLE), c);
}
