#include "machine.h"
#include <math.h>
/*-----------------------------------------------------------------------------------*/
double aa[6] = { 1., 1., 1., 1., 1., 1. };
/*-----------------------------------------------------------------------------------*/
/* used in fydottable.c */
/*-----------------------------------------------------------------------------------*/
int C2F(arnol)(int *neq, double *t, double *y,	double *ydot)
{
	--ydot;
	--y;

	ydot[1] = aa[0] * cos(y[2]) + aa[1] * sin(y[3]);
	ydot[2] = aa[2] * cos(y[3]) + aa[3] * sin(y[1]);
	ydot[3] = aa[4] * cos(y[1]) + aa[5] * sin(y[2]);

	return 0;
}
